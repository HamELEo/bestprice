package ru.bestprice.backend.selenium.entity.QrCode;

public class QrCode {

    private String t;
    private String fp;
    private String fn;
    private String fd;
    private String s;
    private String n;
    private String qr;

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getFp() {
        return fp;
    }

    public void setFp(String fp) {
        this.fp = fp;
    }

    public String getFn() {
        return fn;
    }

    public void setFn(String fn) {
        this.fn = fn;
    }

    public String getFd() {
        return fd;
    }

    public void setFd(String fd) {
        this.fd = fd;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getQr() {
        return qr;
    }

    public void setQr(String qr) {
        this.qr = qr;
    }
}
